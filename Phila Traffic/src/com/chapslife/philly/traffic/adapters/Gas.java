package com.chapslife.philly.traffic.adapters;

public class Gas {
	private String gas_price;
	private String gas_station;
	private String address;
	private String city;
	private String distance;

	/**
	 * @param gas_price the gas_price to set
	 */
	public void setGas_price(String gas_price) {
		this.gas_price = gas_price;
	}

	/**
	 * @return the gas_price
	 */
	public String getGas_price() {
		return gas_price;
	}

	/**
	 * @param gas_station the gas_station to set
	 */
	public void setGas_station(String gas_station) {
		this.gas_station = gas_station;
	}

	/**
	 * @return the gas_station
	 */
	public String getGas_station() {
		return gas_station;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}

	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}

	/**
	 * @param distance the distance to set
	 */
	public void setDistance(String distance) {
		this.distance = distance;
	}

	/**
	 * @return the distance
	 */
	public String getDistance() {
		return distance;
	}
	
}
